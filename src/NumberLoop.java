// Main class, name matching program file name
public class NumberLoop {
    // basic public function declaration
    public static void main(String[] args){
        // Declare variable for loop
        int i;
        // LOOP, starting at 0, incrementing by 1 until i==10 exclusive
        // Each increment value is printed to console
        for(i=0; i<10; i++) System.out.println("i is "+i);
    }
}